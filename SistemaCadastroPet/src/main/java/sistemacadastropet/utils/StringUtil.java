package sistemacadastropet.utils;

/**
 *
 * @author leandro
 */
public class StringUtil {
    public static String primeiraLetraMaiuscula(String str) {
        if (str == null || str.isEmpty()) {
            return str;
        }
        return Character.toUpperCase(str.charAt(0)) + str.substring(1);
    }
}
